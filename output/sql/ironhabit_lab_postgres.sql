CREATE VIEW v_member_training_summary AS
SELECT
    m.member_id,
    m.first_name || ' ' || m.last_name AS member_name,
    COUNT(DISTINCT ws.session_id) AS sessions_2026,
    ROUND(COALESCE(SUM(ps.weight_kg * ps.reps), 0), 2) AS total_volume_kg,
    MAX(ws.session_date) AS last_session_date
FROM members m
LEFT JOIN workout_sessions ws
    ON ws.member_id = m.member_id
    AND ws.session_date BETWEEN DATE '2026-01-01' AND DATE '2026-12-31'
LEFT JOIN performed_sets ps ON ps.session_id = ws.session_id
GROUP BY m.member_id, m.first_name, m.last_name;
