-- IronHabit Lab - PostgreSQL training database
-- Run from an existing PostgreSQL database:
--   psql -d your_database -f output/sql/ironhabit_lab_postgres.sql

DROP SCHEMA IF EXISTS ironhabit_lab CASCADE;
CREATE SCHEMA ironhabit_lab;
SET search_path TO ironhabit_lab;

CREATE TABLE gyms (
    gym_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE,
    city TEXT NOT NULL,
    opened_on DATE NOT NULL
);

CREATE TABLE coaches (
    coach_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    gym_id INTEGER NOT NULL REFERENCES gyms(gym_id),
    first_name TEXT NOT NULL,
    last_name TEXT NOT NULL,
    specialty TEXT NOT NULL,
    hired_on DATE NOT NULL,
    hourly_rate NUMERIC(8, 2) NOT NULL CHECK (hourly_rate > 0)
);

CREATE TABLE members (
    member_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    first_name TEXT NOT NULL,
    last_name TEXT NOT NULL,
    email TEXT NOT NULL UNIQUE,
    birth_date DATE NOT NULL,
    gender CHAR(1) NOT NULL CHECK (gender IN ('F', 'M', 'X')),
    join_date DATE NOT NULL,
    primary_coach_id INTEGER NOT NULL REFERENCES coaches(coach_id),
    home_gym_id INTEGER NOT NULL REFERENCES gyms(gym_id),
    is_active BOOLEAN NOT NULL DEFAULT TRUE
);

CREATE TABLE membership_plans (
    plan_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    plan_name TEXT NOT NULL UNIQUE,
    monthly_fee NUMERIC(8, 2) NOT NULL CHECK (monthly_fee >= 0),
    duration_months INTEGER NOT NULL CHECK (duration_months >= 0),
    includes_classes BOOLEAN NOT NULL DEFAULT FALSE
);

CREATE TABLE member_memberships (
    membership_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    member_id INTEGER NOT NULL REFERENCES members(member_id),
    plan_id INTEGER NOT NULL REFERENCES membership_plans(plan_id),
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    status TEXT NOT NULL CHECK (status IN ('active', 'expired', 'cancelled')),
    CHECK (end_date >= start_date)
);

CREATE TABLE payments (
    payment_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    member_id INTEGER NOT NULL REFERENCES members(member_id),
    membership_id INTEGER REFERENCES member_memberships(membership_id),
    paid_on DATE NOT NULL,
    amount NUMERIC(10, 2) NOT NULL CHECK (amount > 0),
    method TEXT NOT NULL CHECK (method IN ('card', 'cash', 'transfer')),
    status TEXT NOT NULL CHECK (status IN ('paid', 'refunded', 'failed'))
);

CREATE TABLE muscle_groups (
    muscle_group_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE
);

CREATE TABLE exercises (
    exercise_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    muscle_group_id INTEGER NOT NULL REFERENCES muscle_groups(muscle_group_id),
    name TEXT NOT NULL UNIQUE,
    equipment TEXT NOT NULL,
    is_compound BOOLEAN NOT NULL DEFAULT FALSE
);

CREATE TABLE workout_plans (
    workout_plan_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL UNIQUE,
    level TEXT NOT NULL CHECK (level IN ('beginner', 'intermediate', 'advanced')),
    created_by_coach_id INTEGER NOT NULL REFERENCES coaches(coach_id)
);

CREATE TABLE plan_exercises (
    plan_exercise_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workout_plan_id INTEGER NOT NULL REFERENCES workout_plans(workout_plan_id) ON DELETE CASCADE,
    exercise_id INTEGER NOT NULL REFERENCES exercises(exercise_id),
    day_label TEXT NOT NULL,
    exercise_order INTEGER NOT NULL CHECK (exercise_order > 0),
    target_sets INTEGER NOT NULL CHECK (target_sets > 0),
    target_reps_min INTEGER NOT NULL CHECK (target_reps_min > 0),
    target_reps_max INTEGER NOT NULL CHECK (target_reps_max >= target_reps_min),
    target_rpe NUMERIC(3, 1) NOT NULL CHECK (target_rpe BETWEEN 1 AND 10),
    UNIQUE (workout_plan_id, day_label, exercise_order)
);

CREATE TABLE member_plan_assignments (
    assignment_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    member_id INTEGER NOT NULL REFERENCES members(member_id),
    workout_plan_id INTEGER NOT NULL REFERENCES workout_plans(workout_plan_id),
    assigned_by_coach_id INTEGER NOT NULL REFERENCES coaches(coach_id),
    start_date DATE NOT NULL,
    end_date DATE,
    target_sessions_per_week INTEGER NOT NULL CHECK (target_sessions_per_week BETWEEN 1 AND 7),
    CHECK (end_date IS NULL OR end_date >= start_date)
);

CREATE TABLE workout_sessions (
    session_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    member_id INTEGER NOT NULL REFERENCES members(member_id),
    workout_plan_id INTEGER REFERENCES workout_plans(workout_plan_id),
    session_date DATE NOT NULL,
    started_at TIMESTAMP NOT NULL,
    duration_min INTEGER NOT NULL CHECK (duration_min BETWEEN 10 AND 240),
    perceived_rpe NUMERIC(3, 1) NOT NULL CHECK (perceived_rpe BETWEEN 1 AND 10)
);

CREATE TABLE performed_sets (
    set_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    session_id INTEGER NOT NULL REFERENCES workout_sessions(session_id) ON DELETE CASCADE,
    exercise_id INTEGER NOT NULL REFERENCES exercises(exercise_id),
    set_number INTEGER NOT NULL CHECK (set_number > 0),
    weight_kg NUMERIC(7, 2) NOT NULL CHECK (weight_kg >= 0),
    reps INTEGER NOT NULL CHECK (reps > 0),
    rir INTEGER NOT NULL CHECK (rir BETWEEN 0 AND 5),
    is_pr BOOLEAN NOT NULL DEFAULT FALSE,
    UNIQUE (session_id, exercise_id, set_number)
);

CREATE TABLE goals (
    goal_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    member_id INTEGER NOT NULL REFERENCES members(member_id),
    goal_type TEXT NOT NULL,
    target_value NUMERIC(10, 2) NOT NULL CHECK (target_value > 0),
    unit TEXT NOT NULL,
    start_date DATE NOT NULL,
    due_date DATE NOT NULL,
    status TEXT NOT NULL CHECK (status IN ('active', 'completed', 'paused', 'cancelled')),
    CHECK (due_date >= start_date)
);

CREATE TABLE goal_checkins (
    checkin_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    goal_id INTEGER NOT NULL REFERENCES goals(goal_id) ON DELETE CASCADE,
    checked_on DATE NOT NULL,
    current_value NUMERIC(10, 2) NOT NULL CHECK (current_value >= 0),
    notes TEXT
);

CREATE INDEX idx_members_coach_active ON members(primary_coach_id, is_active);
CREATE INDEX idx_memberships_member_status ON member_memberships(member_id, status);
CREATE INDEX idx_payments_paid_on_status ON payments(paid_on, status);
CREATE INDEX idx_sessions_member_date ON workout_sessions(member_id, session_date);
CREATE INDEX idx_sets_session_exercise ON performed_sets(session_id, exercise_id);
CREATE INDEX idx_goal_checkins_goal_date ON goal_checkins(goal_id, checked_on DESC);

